class EntityCristal():
    def __init__(self, id, nombre_material_cristal, nombre_proveedor_cristal, tipo_cristal, precio_cristal, fecha_insercion_cristal, fecha_actualizacion_cristal, estado_cristal) -> None:
        self.id = id
        self.nombre_material_cristal = nombre_material_cristal
        self.nombre_proveedor_cristal = nombre_proveedor_cristal
        self.tipo_cristal = tipo_cristal
        self.precio_cristal = precio_cristal
        self.fecha_insercion_cristal = fecha_insercion_cristal
        self.fecha_actualizacion_cristal = fecha_actualizacion_cristal
        self.estado_cristal = estado_cristal